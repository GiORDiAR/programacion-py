# Decidir si dos palabras son iguales o diferentes. Mostrar mensaje por pantalla:

palabra1=str(input("Ingresar la primera palabra: "))
palabra2=str(input("Ingresar la segunda palabra: "))

if palabra1==palabra2:
    print("Las palabras son iguales.")
else:
    print("Las palabras no son iguales.")

